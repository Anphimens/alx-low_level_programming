#include <stdlib.h>

/**
 * op_add - operator function for adding two int
 * @a: first int
 * @b: second int
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - operator to subtract two ints
 * @a: first int
 * @b: second int
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - operator to multiply two int
 * @a: first int
 * @b: second int
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - operator to divide two int
 * @a: first int
 * @b: second int
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - operator for finding the reminder of an int
 * @a: first int
 * @b: second int
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
